            <!-- Klasifikasi Bunyi -->
            <div>
                <div class="text-left">
                    <p style="font-family: 'Poppins';
                                                        font-style: normal;
                                                        font-weight: 700;
                                                        font-size: 40px;
                                                        line-height: 60px;
